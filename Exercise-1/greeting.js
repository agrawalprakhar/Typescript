// Define the greet function
function greet(name) {
    return "Hello, ".concat(name, "!");
}
// Call the greet function with different inputs
var greeting1 = greet("Prakhar");
console.log(greeting1);
var greeting2 = greet("Rishabh");
console.log(greeting2);
var greeting3 = greet("Prince");
console.log(greeting3);
