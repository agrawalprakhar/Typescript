// Define the greet function
function greet(name: string): string {
    return `Hello, ${name}!`;
}

// Call the greet function with different inputs
const greeting1 = greet("Prakhar");
console.log(greeting1);

const greeting2 = greet("Rishabh");
console.log(greeting2);

const greeting3 = greet("Prince");
console.log(greeting3);
